package org.peie.avicultura.helper;

public class SpeciesObj {

	private String name;
	private String key;

	public SpeciesObj(String name, String key) {
		super();
		this.name = name;
		this.key = key;
	}

	public String getName() {
		return name;
	}

	public String getKey() {
		return key;
	}

}
